# this is called comment
print("Hello \"world\" world")  # use of \"
print('Hello \'world\' world')  # use of \'
print("Line A")
print("Line B")
print("Line A\nLine B")   # use of \n
print("Line A \\n Line B") # print: Line A \n Line B
print(" \\\" \\\' ") #print: \" \'   
# \' --> '  - (a)
# \\ --> \  - (b)
# \\\' --> \'  (a+b)
print("Name\tAkshat")     # use of \t
print("This is Backslash \\")    # printing backslash \
print("This is Double Backslash \\\\")   # printing double backslash \\
print("Hell\blo")  # use of \b
